CREATE TABLE IF NOT EXISTS "public"."books" (
    "id" "uuid" DEFAULT "gen_random_uuid"() NOT NULL,
    "title" "text" NOT NULL,
    "author" "text",
    "status" "text" DEFAULT 'to_read'::"text" NOT NULL,
    "rating" integer,
    "created_at" timestamp with time zone DEFAULT "now"() NOT NULL,
    "updated_at" timestamp with time zone DEFAULT "now"() NOT NULL,
    CONSTRAINT "books_author_length" CHECK ((("author" IS NULL) OR ("char_length"("author") <= 200))),
    CONSTRAINT "books_rating_range" CHECK ((("rating" IS NULL) OR (("rating" >= 1) AND ("rating" <= 5)))),
    CONSTRAINT "books_status_valid" CHECK (("status" = ANY (ARRAY['to_read'::"text", 'reading'::"text", 'finished'::"text"]))),
    CONSTRAINT "books_title_length" CHECK ((("char_length"("btrim"("title")) >= 1) AND ("char_length"("btrim"("title")) <= 200)))
);


